/**
 * scripts/update-versions.mjs
 * - Fetches Mojang manifest
 * - Extracts type === 'release' version ids
 * - Sorts descending, dedupes
 * - Writes src/renderer/constants/versionOptions.ts if content changed
 * - Emits the top 3 detected versions on stdout as JSON for the workflow to capture
 */

import https from 'https';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const MANIFEST_URL = 'https://piston-meta.mojang.com/mc/game/version_manifest_v2.json';
const OUT_PATH = path.join(__dirname, '..', 'src', 'renderer', 'constants', 'versionOptions.ts');

function fetchJson(url) {
  return new Promise((res, rej) => {
    https.get(url, (r) => {
      let data = '';
      r.on('data', c => data += c);
      r.on('end', () => {
        try { res(JSON.parse(data)); } catch (e) { rej(e); }
      });
    }).on('error', rej);
  });
}

function semverCompareDesc(a, b) {
  const pa = a.split('.').map(Number);
  const pb = b.split('.').map(Number);
  for (let i = 0; i < Math.max(pa.length, pb.length); i++) {
    const na = pa[i] || 0;
    const nb = pb[i] || 0;
    if (na !== nb) return nb - na; // descending
  }
  return 0;
}

try {
  const manifest = await fetchJson(MANIFEST_URL);
  const releases = (manifest.versions || []).filter(v => v.type === 'release').map(v => v.id);
  const unique = Array.from(new Set(releases));
  unique.sort(semverCompareDesc);

  // write file content
  const header = `// This file is auto-generated by .github/workflows/update-versions.yml\n`;
  const content = `${header}export const VERSION_OPTIONS: string[] = [\n` +
    unique.map(v => `  '${v}',`).join('\n') +
    `\n];\n`;

  let existing = '';
  if (fs.existsSync(OUT_PATH)) existing = fs.readFileSync(OUT_PATH, 'utf8');

  if (existing.trim() === content.trim()) {
    console.log(JSON.stringify({ changed: false, detected: unique.slice(0, 3) }));
    process.exit(0);
  } else {
    fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
    fs.writeFileSync(OUT_PATH, content, 'utf8');
    console.log(JSON.stringify({ changed: true, detected: unique.slice(0, 3) }));
    process.exit(0);
  }
} catch (e) {
  console.error('error', e && e.message ? e.message : e);
  process.exit(2);
}
